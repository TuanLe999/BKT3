package com.example.bkt3;

public class Thi {
       private String ten;
       private String viDu;

       public Thi(String ten, String viDu) {
              this.ten = ten;
              this.viDu = viDu;
       }

       public Thi() {
       }

       public String getTen() {
              return ten;
       }

       public void setTen(String ten) {
              this.ten = ten;
       }

       public String getViDu() {
              return viDu;
       }

       public void setViDu(String viDu) {
              this.viDu = viDu;
       }
}
